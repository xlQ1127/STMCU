 void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
   if( GPIO_Pin == GPIO_PIN_0 )
			{
				 if( HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET )
			    {

							}			
			}
			
   if( GPIO_Pin == KEY_CLOSE_Pin )
			{
				 if( HAL_GPIO_ReadPin(KEY_CLOSE_GPIO_Port, KEY_CLOSE_Pin) == GPIO_PIN_SET && DoorStatus == DOOR_OPEN)
			    {
					   	DoorCMD=DOOR_CLOSE;
							}
			}			
			
	  if( GPIO_Pin == KEY_OPEN_Pin )
			{
				 if( HAL_GPIO_ReadPin(KEY_OPEN_GPIO_Port, KEY_OPEN_Pin) == GPIO_PIN_SET && DoorStatus == DOOR_CLOSE )
			    {
					   	DoorCMD=DOOR_OPEN;
							}			
			}			
	
}

